    private void jButton_saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_saveActionPerformed
        // TODO add your handling code here:
        java.sql.Connection conn = new DBkoneksi().connect();
        try {
            java.sql.PreparedStatement stmt = conn.prepareStatement("insert into tmhs (nim,nama,prodi)values(?,?,?)");
            try {
                stmt.setString(1, jTextField_nim.getText());
                stmt.setString(2, jTextField_nama.getText());
                stmt.setString(3, jComboBox_prodi.getSelectedItem().toString());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "data berhasil disimpan","pesan",JOptionPane.INFORMATION_MESSAGE);
                TampilData();
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "data gagal disimpan","pesan",JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton_saveActionPerformed

