    private void jButton_cari_okActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_cari_okActionPerformed
        // TODO add your handling code here:
        DftTblModel_mahasiswa = new DefaultTableModel();
        DftTblModel_mahasiswa.addColumn("NIM");
        DftTblModel_mahasiswa.addColumn("NAMA");
        DftTblModel_mahasiswa.addColumn("PROGRAM STUDI");
        jTable_mhs.setModel(DftTblModel_mahasiswa);
        java.sql.Connection conn = new DBkoneksi().connect();
        try {
            java.sql.Statement stmt = conn.createStatement();
            if (jComboBox_kategoripencarian.getSelectedItem().equals("NIM")) {
                SQL = "select * from tmhs where nim like '%"+jTextField_cari.getText()+"%'";
                
            } else if(jComboBox_kategoripencarian.getSelectedItem().equals("NAMA")){
                SQL = "select * from tmhs where nama like '%"+jTextField_cari.getText()+"%'";
            }
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_mahasiswa.addRow(new Object[]{
                    res.getString("NIM"),
                    res.getString("NAMA"),
                    res.getString("PRODI")
                });
                
            }
            
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton_cari_okActionPerformed

