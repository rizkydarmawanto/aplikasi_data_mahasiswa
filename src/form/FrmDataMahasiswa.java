    private void jButton_updateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_updateActionPerformed
        // TODO add your handling code here:
        java.sql.Connection conn = new DBkoneksi().connect();
        try {
            java.sql.PreparedStatement stmt = conn.prepareStatement("update tmhs set nama =?,prodi=? where nim = ?");
            try {
                stmt.setString(1, jTextField_nama.getText());
                stmt.setString(2, jComboBox_prodi.getSelectedItem().toString());
                stmt.setString(3, jTextField_nim.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null,"data berhasil di update","pesan",JOptionPane.INFORMATION_MESSAGE);
                TampilData();
            } catch (HeadlessException | SQLException e) {
                JOptionPane.showMessageDialog(null, "data gagal di update","pesan",JOptionPane.INFORMATION_MESSAGE);
                
            }
        } catch (HeadlessException | SQLException e) {
        }
    }//GEN-LAST:event_jButton_updateActionPerformed

    private void jButton_deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton_deleteMouseClicked
        // TODO add your handling code here:
        java.sql.Connection conn = new DBkoneksi().connect();
        int ok = JOptionPane.showConfirmDialog(null, "apakah anda yakin menghapus data ?","konfirmasi",JOptionPane.YES_NO_OPTION);
        if (ok==0) {
            try {
            SQL = "delete from tmhs where nim =' "+jTextField_nim.getText()+"'";
            java.sql.PreparedStatement stmt = conn.prepareStatement(SQL);
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "data berhasil di hapus");
            TampilData();
            jTextField_nim.setText("");
            jTextField_nama.setText("");
            jComboBox_prodi.setSelectedItem("PILIH :");
        } catch (HeadlessException | SQLException ex) {
            JOptionPane.showMessageDialog(null, "gagal di hapus");
       }
        }
    }//GEN-LAST:event_jButton_deleteMouseClicked

